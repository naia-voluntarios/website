	/// MAILCHIMP REGISTER
	var request;
	function register(event){

      // Prevent default posting of form - put here to work in case of errors
      event.preventDefault();

      // Abort any pending request
      if (request) {
          request.abort();
      }
      // setup some local variables
      var $form = $(this);

      

      // Let's select and cache all the fields
      var $inputs = $form.find("input, select, button, textarea");

      // Serialize the data in the form
      var serializedData = $form.serialize();

      // Let's disable the inputs for the duration of the Ajax request.
      // Note: we disable elements AFTER the form data has been serialized.
      // Disabled form elements will not be serialized.
      $inputs.prop("disabled", true);

      // Fire off the request to /form.php
      request = $.ajax({
          url: "subscribe.php",
          type: "post",
          data: serializedData
      });

      // Callback handler that will be called on success
      request.done(function (response, textStatus, jqXHR){
          // Log a message to the console
          var obj = JSON.parse(response);
          console.log(obj.senderEmail);
          if(obj.result === 0){
          	window.location.href = 'https://pagseguro.uol.com.br/checkout/v2/donation.html?receiverEmail=henrique.carrasco@outlook.com&currency=BRL&donationValue=50&user='+obj.senderEmail;
          }
      });


      // Callback handler that will be called on failure
      request.fail(function (jqXHR, textStatus, errorThrown){
          // Log the error to the console
          console.error("The following error occurred: "+textStatus +" " + errorThrown);
      });

      // Callback handler that will be called regardless
      // if the request failed or succeeded
      request.always(function () {
          // Reenable the inputs
          $inputs.prop("disabled", false);
      });

	}
	
	$('#signup').submit(register);
  
